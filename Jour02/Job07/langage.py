def développeur(langage):
    if langage == "javascript":
        result = "tu es un développeur web"
    elif langage == "python":
        result = "tu es un développeur IA"
    elif langage == "java":
        result = "tu es un développeur logiciel"
    elif langage == "reactjs":
        result = "tu es un développeur mobile"
    else: 
        result = "un jour, je serai le meilleur développeur"
    print(result)

développeur("javascript")
développeur("python")
développeur("java")
développeur("reactjs")
